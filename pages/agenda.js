import Head from "next/head";
import NavBarTop from "../components/NavBarTop";
import Footer from "../components/Footer";
import Breadcrumb from "../components/Breadcrumb";
import AgendaCard from "../components/AgendaCard";

const title = "Agenda - Next Bootstrap";

export default function Blog({agendas}) {
    return (
        <>
            <style jsx>
                {`
                main {
                    margin-top: 60px;
                    min-height: 100vh;
                }
            `}
            </style>

            <Head>
                <title>{title}</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>

            <NavBarTop />

            <main>
                <div className="bg-light">
                    <Breadcrumb pageName="Agenda" currentPage="Agenda" />
                </div>

                <div className="container my-5">
                    <div className="row g-4">
                        {agendas.map(agenda =>
                            <div className="col-lg-6" key={agenda.id}>
                                <AgendaCard 
                                    id={agenda.id} 
                                    slug={agenda.slug}
                                    image={agenda.image} 
                                    title={agenda.title} 
                                    location={agenda.location}
                                    date={agenda.date} 
                                    time={agenda.time}  />
                            </div>
                        )}
                    </div>
                </div>
            </main>

            <Footer />
        </>
    );
};

// This gets called on every request to this page
export async function getServerSideProps() {
    const getAllAgenda = await fetch(`${process.env.NEXT_PUBLIC_API_URL}/api/agenda`);
    const agendas = await getAllAgenda.json();
    return {
        props: { agendas }, // will be passed to the page component as props
    };
};